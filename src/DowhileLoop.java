
public class DowhileLoop {

	public static void main(String[] args) {
		
		int i=20;
		
		do {
			
			System.out.println(i);
			i++;
			
		}while(i<30); //In do while, 1 loop of execution is guarantee. In the while loop, 1st need condition to execute but for do while for 1st time no need any condition.

	}

}
